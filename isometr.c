#include "fdf.h"

void	isometr(t_fdf d)
{
	d->x1 = (d->x1 - d->y1) * cos(0.8);
	d->y1 = (d->x1 + d->y1) * sin(0.8) - d->z;
	d->x2 = (d->x2 - d->y2) * cos(0.8);
	d->y2 = (d->x2 + d->y2) * sin(0.8) - d->z1;
}

void	reset_coord(t_fdf d)
{
	d->x1 = 0;
	d->x2 = 0;
	d->y1 = 0;
	d->y2 = 0;
	d->z = 0;
	d->z1 = 0;
}
